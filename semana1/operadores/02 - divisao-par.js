// Pedindo para adicionar um número par:
numeroPar = Number(prompt('Olá, tudo bem? Insira um número par, por favor:'))

// Criando uma variável para armazenar o resto da divisão por 2:
restoDivisao = numeroPar % 2

// Impressão do resto do número:
console.log('O resto da divisão desse número por 2 é:', restoDivisao)

// Quando o número é par, o resto da divisão por 2 sempre será 0. Todavia, quando o número for ímpar, o resto da divisão será diferente de 0.